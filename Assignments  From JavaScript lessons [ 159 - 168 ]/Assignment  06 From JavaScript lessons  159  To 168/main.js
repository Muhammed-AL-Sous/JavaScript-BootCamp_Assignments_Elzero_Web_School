function* gen() {
  let firstNum = 14;
  let increaseNum = 140;
  let sum = firstNum + increaseNum;
  yield firstNum;
  while (true) {
    yield sum;
    sum += 200 + increaseNum;
    increaseNum += 200;
  }
}

let generator = gen();

console.log(generator.next()); // {value: 14, done: false}
console.log(generator.next()); // {value: 154, done: false}
